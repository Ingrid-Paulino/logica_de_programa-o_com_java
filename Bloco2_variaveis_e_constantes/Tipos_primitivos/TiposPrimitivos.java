public class TiposPrimitivos {
  public static void main(String[] args) {

    // Principal Diferença ente tipo normal e primitivo:
       // 1 - normal: pode estar em um estado de ausencia de valor. EX: Byte variavelByte = null;
       // 2 - primitivo: não aceita valores nulos
       // 3 - Não existe primitivo de string
       
    // Byte variavelByte = 1; // Tipo normal
    byte variavelByte = 1;  // Tipo primitivo

    // Short variavelShort = 1;
    short variavelShort = 1;

    // Integer variavelInterger = 1;
    int variavelInterger = 1;

    // Long variavelLong = 1L;
    long variavelLong = 1L;

    // Float variavelFloat = 1.0f;
    float variavelFloat = 1.0f;

    // Double variavelDouble = 1.0;
    double variavelDouble = 1.0;

    // Character variavelCharacter = 'A';
    char variavelCharacter = 'A';

    // Boolean variavelBoolean = true;
    boolean variavelBoolean = true;
  }
}
